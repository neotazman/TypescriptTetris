function findStartingPosition(piece) {
    let result;
    for (let i = 0; i < piece.length; i++) {
        let currentRow = piece[i];
        let found = currentRow.findIndex((cell) => cell === 1);
        if (found === -1)
            continue;
        else
            result = [i, found];
    }
    return result;
}
